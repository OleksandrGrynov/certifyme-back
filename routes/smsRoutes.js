function normalizePhone(phone) {
    if (!phone) return null;
    const digits = phone.replace(/[^0-9]/g, "");
    if (!digits) return null;
    return digits.startsWith("380")
        ? `+${digits}`
        : digits.startsWith("+")
            ? digits
            : `+${digits}`;
}

