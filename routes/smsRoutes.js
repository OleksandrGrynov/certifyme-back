router.post("/subscribe", verifyToken, async (req, res) => {
    try {
        const userId = req.user.id;
        const rawPhone = req.body.phone;
        const phone = normalizePhone(rawPhone);

        if (!phone)
            return res
                .status(400)
                .json({ success: false, message: "Phone required" });

        // —É–Ω–∏–∫–∞—î–º–æ –¥—É–±–ª—ñ–∫–∞—Ç—ñ–≤
        const existing = await prisma.smsSubscription.findFirst({
            where: {
                OR: [{ userId }, { phone }],
            },
        });

        if (existing)
            return res.json({
                success: true,
                message: "Already subscribed",
            });

        await prisma.smsSubscription.create({
            data: { userId, phone },
        });

        res.json({ success: true, message: "Subscription saved" });
    } catch (err) {
        console.error("‚ùå Subscribe error:", err);
        res.status(500).json({ success: false, message: "Server error" });
    }
});

/* ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
   üì£ 3. –ê–¥–º—ñ–Ω—Å—å–∫–∞ —Ä–æ–∑—Å–∏–ª–∫–∞ SMS —á–µ—Ä–µ–∑ Twilio
   ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ */
router.post("/send-promo", verifyToken, isAdmin, async (req, res) => {
    try {
        const { message } = req.body;
        if (!message?.trim())
            return res.status(400).json({
                success: false,
                message: "–ü–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –Ω–µ –º–æ–∂–µ –±—É—Ç–∏ –ø–æ—Ä–æ–∂–Ω—ñ–º",
            });

        // –æ—Ç—Ä–∏–º—É—î–º–æ —É–Ω—ñ–∫–∞–ª—å–Ω—ñ —Ç–µ–ª–µ—Ñ–æ–Ω–∏
        const subs = await prisma.smsSubscription.findMany({
            select: { phone: true },
        });
        if (!subs.length)
            return res.json({
                success: false,
                message: "–ù–µ–º–∞—î –ø—ñ–¥–ø–∏—Å–∞–Ω–∏—Ö –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—ñ–≤",
            });

        const uniquePhones = [...new Set(subs.map((s) => normalizePhone(s.phone)))];

        let sent = 0;
        let failed = [];

        for (const phone of uniquePhones) {
            const result = await sendSMS(phone, message);
            if (result?.success) sent++;
            else failed.push(phone);
        }

        res.json({
            success: true,
            message: `‚úÖ –†–æ–∑—Å–∏–ª–∫–∞ –≤–∏–∫–æ–Ω–∞–Ω–∞: ${sent}/${uniquePhones.length} –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω—å`,
            failed: failed.length ? failed : undefined,
        });
    } catch (err) {
        console.error("‚ùå sendPromo error:", err);
        res.status(500).json({ success: false, message: "Server error" });
    }
});

